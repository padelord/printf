#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	nd;

	nd = n >> 3;
	n &= 0x00000007;
	while (nd--)
	{
		*((long int*)(s)) = 0l;
		s += 8;
	}
	while (n--)
	{
		*((unsigned char*)(s)) = 0;
		s++;
	}
}
